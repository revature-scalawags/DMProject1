package Project1

class VGSale {

        private var rank: Int = _
        private var name: String = _
        private var platform: String = _
        private var year: Int = _
        private var genre: String = _
        private var publisher: String = _
        private var na_Sales: Double = _
        private var eu_Sales: Double = _
        private var jp_Sales: Double = _
        private var other_Sales: Double = _
        private var global_Sales: Double = _





