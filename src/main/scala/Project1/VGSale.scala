        def get_Name(): String={ name }

        def set_Platform(x: String){ platform = x }
        def get_Platform(): String={ platform }

        def set_Year(x: Int){ year = x }
        def get_Year(): Int={ year }

        def set_Genre(x: String){ genre = x }
        def get_Genre(): String={ genre }

        def set_Publisher(x: String){ publisher = x }
        def get_Publisher(): String={ publisher }

        def set_Na_Sales(x: Double){ na_Sales = x }
        def get_Na_Sales(): Double={ na_Sales }

        def set_Eu_Sales(x: Double){ eu_Sales = x }
        def get_Eu_Sales(): Double={ eu_Sales }

        def set_Jp_Sales(x: Double){ jp_Sales = x }
        def get_Jp_Sales(): Double={ jp_Sales }

        def set_Other_Sales(x: Double){ other_Sales = x }
        def get_Other_Sales(): Double={ other_Sales }

        def set_Global_Sales(x: Double){ global_Sales = x }
        def get_Global_Sales(): Double={ global_Sales }
             

}
